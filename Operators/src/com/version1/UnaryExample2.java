package com.version1;

public class UnaryExample2 {
	public static void main(String[] args) {
		int x = 10;
		int y = 20;
		
		System.out.println(x++);
		
		System.out.println(x);
		
		System.out.println(++x);
		
		System.out.println(--y);
		
		System.out.println(y--);
		
		System.out.println(--y);
	}

}
